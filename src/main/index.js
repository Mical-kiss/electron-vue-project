import { app, BrowserWindow, Tray, Notification, Menu } from 'electron'

/**
 * Set `__static` path to static files in production
 * https://simulatedgreg.gitbooks.io/electron-vue/content/en/using-static-assets.html
 */
if (process.env.NODE_ENV !== 'development') {
  global.__static = require('path').join(__dirname, '/static').replace(/\\/g, '\\\\')
}

let mainWindow
const winURL = process.env.NODE_ENV === 'development'
  ? `http://localhost:9080`
  : `file://${__dirname}/index.html`

function createWindow () {
  const appIcon = new Tray('/Users/zx/Downloads/00chromeDonwload/矩形@2x.png')
  mainWindow = new BrowserWindow({
    height: 563,
    useContentSize: false,
    width: 1000,
    icon: '/Users/zx/Downloads/tinypng_output/b.png'
  })
  const contextMenu = Menu.buildFromTemplate([
    { label: 'Item1', type: 'radio' },
    { label: 'Item2', type: 'radio' },
    { label: 'Item3', type: 'radio', checked: true },
    { label: 'Item4', type: 'radio' }
  ])
  appIcon.setToolTip('This is my application.')
  appIcon.setContextMenu(contextMenu)
  appIcon.on('mouse-enter', () => {
    console.log(111)
  })
  setTimeout(() => {
    const myNotice = new Notification({
      title: '重要通知',
      body: '明天之后放假',
      sound: ''
    })
    // myNotice.show()
    console.log(myNotice)
  }, 2000)
  mainWindow.loadURL(winURL)

  mainWindow.on('closed', () => {
    mainWindow = null
  })
}

app.on('ready', createWindow)

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit()
  }
})

app.on('activate', () => {
  if (mainWindow === null) {
    createWindow()
  }
})

